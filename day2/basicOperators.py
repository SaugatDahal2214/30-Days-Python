##Integer Operations

a = 10
b = 5

addition = a+b
sub = a-b
mul = a*b
div = a/b

print(addition, sub, mul, int(div))

##Float Operations

x = 4.5
y = 1.23

float_add = x+y
float_sub = x+y
float_mul = x+y
float_div = x+y

print(float_add, float_sub, float_mul, float_div)

##String Operation

greeting = "Hello, World!"
print(len(greeting))

upperCase = greeting.upper()
print(upperCase)

subString = greeting[7:13]
print(subString)

##Boolean Operation
is_sunny = True
is_raining = False

##Logical AND operation
logicalAnd = is_sunny and is_raining
print(logicalAnd)

##Logical OR operation
logicalOr = is_sunny or is_raining
print(logicalOr)